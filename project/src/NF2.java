        public static boolean time(JTextField f){
            
           DateFormat formatter = new SimpleDateFormat("dd/MM/yyyy"); 
           formatter.setLenient(false);
           try{
               Date date = formatter.parse(f.getText());
               return true;
           } catch (ParseException ex) {
               NF2 n = new NF2();
               JOptionPane.showConfirmDialog(n, "The date format must be(XX/MM/YYYY)");
              
               return false;
               
            }
        } 
        
        
