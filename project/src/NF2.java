    public class check_text extends check_time{
        public void check(){
            try{
                if(jTextField1.getText().trim().isEmpty() || jTextField2.getText().trim().isEmpty()|| jTextField3.getText().trim().isEmpty() ){
                    throw new NullPointerException("Empty Feild");
                }
                else{
                    if(check_text.time(jTextField3)){
                        NF.AddRows(new Object[]{jTextField1.getText(),jTextField2.getText(),jTextField3.getText()}); 
                        Reminder r = new Reminder();
                        r.remind(jTextField3.getText(), jTextField1.getText(), jTextField2.getText());
                    }
                       
                }
                          
            }
            catch(NullPointerException e){
                NF2 n = new NF2();
                JOptionPane.showConfirmDialog(n, "Somthing Wrong!!, there is an Empty TextFeild ");
            }
        }
    }
    public class Reminder{
        public void remind(String tf, String tf1, String tf2){
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate todayDate = LocalDate.now();
            String date = todayDate.format(formatter);
            if(date.equals(tf)){
                JFrame gf = new JFrame("Reminder");
                gf.setVisible(true);
                gf.setSize(480, 350);
                gf.add(new JTextArea("Dear User,\n\n"
                + "You have entered a task on This Date: " + tf + "\n"
                + "which is: " + tf1 + "\n\n"
                + "We wanted to remind you to complete it before 12 o'clock (midnight).\n\n"
                + "Please remember that a note has been placed on it: " + tf2));

            }
            
        }
    }
